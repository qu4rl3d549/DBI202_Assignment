CREATE TABLE KHACHHANG
(
	MAKH VARCHAR(10) NOT NULL PRIMARY KEY,
	TENKH NVARCHAR(255),
	SDT VARCHAR(15) ,
	DIACHI NVARCHAR(255)
);

CREATE TABLE CHINHANH
(
	MACN VARCHAR(10) NOT NULL PRIMARY KEY,
	TENCN NVARCHAR(255),
	DOANHTHU DECIMAL(18,2),
	DIACHI NVARCHAR(255),
	MANQLCN VARCHAR(10) 
);

CREATE TABLE NHANVIEN
(
	MANV VARCHAR(10) NOT NULL PRIMARY KEY,
	TENNV NVARCHAR(255),
	EMAIL VARCHAR(255) ,
	LUONG DECIMAL(18,2),
	MACN VARCHAR(10),
	CONSTRAINT FK_MACN FOREIGN KEY (MACN) REFERENCES CHINHANH(MACN)
);

CREATE TABLE CAPBACTHANHVIEN
(
	MATV VARCHAR(10) NOT NULL PRIMARY KEY,
	CAPBAC NVARCHAR(50),
	MUCGIAMGIA INT
);

CREATE TABLE TAIKHOAN
(
	MATK VARCHAR(10) NOT NULL PRIMARY KEY,
	TENDANGNHAP NVARCHAR(255) UNIQUE,
	MATKHAU NVARCHAR(255),
	VAITRO NVARCHAR(50),
	MATV VARCHAR(10),
	MAKH VARCHAR(10),
	MANV VARCHAR(10),
	MANQLTK VARCHAR(10),
	CONSTRAINT FK_MAKH FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH),
	CONSTRAINT FK_MANV FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
	CONSTRAINT FK_MANQLTK FOREIGN KEY (MANQLTK) REFERENCES NHANVIEN(MANV),
	CONSTRAINT FK_MATV FOREIGN KEY (MATV) REFERENCES CAPBACTHANHVIEN(MATV)
);

CREATE TABLE SANPHAM
(
	MASP VARCHAR(10) NOT NULL PRIMARY KEY,
	TENSP NVARCHAR(255),
	MOTA NVARCHAR(MAX),
	PHANLOAI NVARCHAR(255),
	GIA DECIMAL(18,2),
	MANSX VARCHAR(10),
	DONVITINH NVARCHAR(50),
	CONSTRAINT FK_MANSX FOREIGN KEY (MANSX) REFERENCES NHASANXUAT(MANSX)
);

CREATE TABLE DANHGIA
(
	MATK VARCHAR(10) NOT NULL,
    MASP VARCHAR(10) NOT NULL,
    MADG VARCHAR(10) NOT NULL,
    DG INT,
    CONSTRAINT PR_MADG_MATK_MASP PRIMARY KEY (MATK, MASP, MADG),
	CONSTRAINT FK_MATK2 FOREIGN KEY (MATK) REFERENCES TAIKHOAN(MATK),
    CONSTRAINT FK_MASP2 FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP)
);

CREATE TABLE KHOCHINHANH
(
	MAKHO VARCHAR(10) NOT NULL PRIMARY KEY,
	DIACHI NVARCHAR(255),
	MACN VARCHAR(10),
	MANQLK VARCHAR(10),
	CONSTRAINT FK_MACN2 FOREIGN KEY (MACN) REFERENCES CHINHANH(MACN),
	CONSTRAINT FK_MANQLK FOREIGN KEY (MANQLK) REFERENCES NHANVIEN(MANV)
);

CREATE TABLE LUUTRU
(
	MAKHO VARCHAR(10) NOT NULL,
	MASP VARCHAR(10) NOT NULL,
	SOLUONGHANGTON INT,
	CONSTRAINT PR_MAKHO_MASP PRIMARY KEY (MAKHO, MASP),
	CONSTRAINT FK_MAKHO FOREIGN KEY (MAKHO) REFERENCES KHOCHINHANH(MAKHO),
	CONSTRAINT FK_MASP FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP)
);

CREATE TABLE NHASANXUAT
(
	MANSX VARCHAR(10) NOT NULL PRIMARY KEY,
	TENNSX NVARCHAR(255),
	DIACHI NVARCHAR(255),
	SDT VARCHAR(15) UNIQUE
);

CREATE TABLE HOADON
(
	MAHD VARCHAR(10) NOT NULL PRIMARY KEY,
	TONGTIENTRUOCGIAMGIA DECIMAL(18,2),
	SOTIENGIAMGIA DECIMAL(18,2),
	TONGTIENSAUGIAMGIA DECIMAL(18,2),
	NGAYTAOHD DATE,
	MAKH VARCHAR(10),
	MANV VARCHAR(10),
	MACN VARCHAR(10),
	MATK VARCHAR(10),
	CONSTRAINT FK_MAKH2 FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH),
	CONSTRAINT FK_MANV2 FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
	CONSTRAINT FK_MACN3 FOREIGN KEY (MACN) REFERENCES CHINHANH(MACN),
	CONSTRAINT FK_MATK FOREIGN KEY (MATK) REFERENCES TAIKHOAN(MATK)
);

CREATE TABLE CHITIETHOADON
(
	MAHD VARCHAR(10) NOT NULL,
	MASP VARCHAR(10) NOT NULL,
	SOLUONGSP INT,
	THANHTIEN DECIMAL(18,2),
	CONSTRAINT PR_MAHD_MASP PRIMARY KEY (MAHD, MASP),
	CONSTRAINT FK_MAHD FOREIGN KEY (MAHD) REFERENCES HOADON(MAHD),
	CONSTRAINT FK_MASP3 FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP)
);



